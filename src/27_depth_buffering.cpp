        // 我们不需要显式地将图像的布局转换为深度附件，因为我们将会修改 RenderPass 以包含深度附件，而 RenderPass 的 SubPass 会自动进行图像布局转换，由 SubPass 的依赖所决定 (VkSubpassDependency)
